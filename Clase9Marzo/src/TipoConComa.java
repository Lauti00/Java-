public class TipoConComa {
    public static void main(String[] args) {
        float numeroFloat= (float)3.456E23;
        System.out.println("numeroFloat = " + numeroFloat);
        System.out.println("El valor minimo de float es=" + " "+ Float.MIN_VALUE);
        System.out.println("El valor maximo de float es =" + " "+ Float.MAX_VALUE);
    
        
        double numeroDouble=1.7484848 ;
        System.out.println("numeroDouble = " + numeroDouble);
        System.out.println("El numero minimo de Double es=" + " "+ Double.MIN_VALUE);
        System.out.println("El numero maximo de Double es=" +" " + Double.MAX_VALUE);  
    }
    
}
